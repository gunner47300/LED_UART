void set_led_brightness(TIM_HandleTypeDef * timer, uint32_t channel,
		double brightness) {
	int32_t value = powf(brightness, 2.2) * 9999;
	__HAL_TIM_SET_COMPARE(timer, channel, value);
}
typedef struct {
	double red;
	double green;
	double blue;
} rgb;
typedef struct {
	double hue;
	double saturation;
	double value;
} hsv;
rgb hsv2rgb(hsv in) {
	if (in.hue >= 360)
		in.hue = 0;
	double c = in.value * in.saturation;
	double x = c * (1 - fabs(fmod((in.hue / 60), 2) - 1));
	double m = in.value - c;
	rgb out;
	switch ((int) (in.hue / 60)) {
	case 0:
		out.red = c + m;
		out.green = x + m;
		out.blue = 0;
		break;
	case 1:
		out.red = x + m;
		out.green = c + m;
		out.blue = 0;
		break;
	case 2:
		out.red = 0;
		out.green = c + m;
		out.blue = x + m;
		break;
	case 3:
		out.red = 0;
		out.green = x + m;
		out.blue = c + m;
		break;
	case 4:
		out.red = x + m;
		out.green = 0;
		out.blue = c + m;
		break;
	case 5:
		out.red = c + m;
		out.green = 0;
		out.blue = x + m;
		break;
	}
	return out;
}
void set_color(hsv color_hsv) {
	rgb color_rgb = hsv2rgb(color_hsv);
	set_led_brightness(&htim2, TIM_CHANNEL_3, color_rgb.red);
	set_led_brightness(&htim8, TIM_CHANNEL_2, color_rgb.green);
	set_led_brightness(&htim8, TIM_CHANNEL_1, color_rgb.blue);
	red = color_rgb.red;
	green = color_rgb.green;
	blue = color_rgb.blue;
}
